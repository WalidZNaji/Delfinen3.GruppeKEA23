public class Restance {

    private String name;
    private int medlemID;

    public Restance(String name, int medlemID) {
        this.name = name;
        this.medlemID = medlemID;
    }
    public Restance() {
    }

    public String getName() {
        return name;
    }

    public int getMedlemID() {
        return medlemID;
    }
}
